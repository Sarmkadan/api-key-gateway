    /// <summary>
    /// Gets the time unit description for rate limit error messages.
    /// </summary>
    private static string GetTimeUnit(int seconds) => seconds switch
    {
        1 => "second",
        60 => "minute",
        3600 => "hour",
        86400 => "day",
        _ => $"{seconds} seconds"
    };

    /// <summary>
    /// Writes a ProblemDetails response to the HTTP context with proper content type.
    /// </summary>
    /// <param name="context">The HTTP context.</param>
    /// <param name="problemDetails">The ProblemDetails to write.</param>
    /// <returns>A task representing the asynchronous operation.</returns>
    public static async Task WriteProblemAsync(this HttpContext context, ProblemDetails problemDetails)
    {
        ArgumentNullException.ThrowIfNull(context);
        ArgumentNullException.ThrowIfNull(problemDetails);

        context.Response.ContentType = MediaTypeNames.Application.ProblemJson;
        context.Response.StatusCode = problemDetails.Status ?? StatusCodes.Status500InternalServerError;

        // Set Retry-After header for 429 responses
        if (problemDetails.Status == StatusCodes.Status429TooManyRequests &&
            problemDetails.Extensions.TryGetValue("retryAfter", out var retryAfter))
        {
            if (retryAfter is int retryAfterInt)
            {
                context.Response.Headers.RetryAfter = retryAfterInt.ToString();
            }
            else if (retryAfter is DateTime retryAfterDate)
            {
                var seconds = (int)(retryAfterDate - DateTime.UtcNow).TotalSeconds;
                context.Response.Headers.RetryAfter = seconds.ToString();
            }
        }

        await context.Response.WriteAsJsonAsync(problemDetails);
    }

