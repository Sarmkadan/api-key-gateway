/// <summary>
/// Factory for creating RFC 7807 ProblemDetails responses.
/// Implements the ProblemDetails format as specified in RFC 7807 for standardized error responses.
/// </summary>
/// <remarks>
/// RFC 7807 defines a standardized format for HTTP API error responses:
/// - type: A URI reference that identifies the problem type (SHOULD be dereferenceable)
/// - title: A short, human-readable summary of the problem type
/// - status: The HTTP status code generated by the origin server
/// - detail: A human-readable explanation specific to this occurrence of the problem
/// - instance: A URI reference that identifies the specific occurrence of the problem
/// </remarks>
public static class GatewayProblemDetailsFactory
