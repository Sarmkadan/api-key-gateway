    /// <summary>
    /// Creates a ProblemDetails response for invalid API key errors.
    /// </summary>
    /// <param name="context">The HTTP context.</param>
    /// <param name="message">The error detail message.</param>
    /// <param name="instance">The request URI that caused the error.</param>
    /// <returns>A ProblemDetails object ready for serialization.</returns>
    public static ProblemDetails CreateInvalidKeyProblem(
        HttpContext context,
        string message,
        string? instance = null)
    {
        ArgumentNullException.ThrowIfNull(context);
        ArgumentException.ThrowIfNullOrWhiteSpace(message);

        return new ProblemDetails
        {
            Type = "urn:api-key-gateway:invalid-key",
            Title = "Invalid API Key",
            Status = StatusCodes.Status401Unauthorized,
            Detail = message,
            Instance = instance ?? context.Request.Path.ToString()
        };
    }

